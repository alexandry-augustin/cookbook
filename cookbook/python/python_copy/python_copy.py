import copy
#----------------------------------------------------------
def copy_test():
	l=range(3)
	a=copy.copy(l)
#----------------------------------------------------------
def deepcopy_test():
	a=0
	b=a
	a=1
	assert(b==0)
	#--------------------------------------------------
	a=range(3)
	b=copy.deepcopy(a)
	a[0]=5
	assert(a==[5,1,2])
	assert(b==range(3))
	#--------------------------------------------------
	a=[[0,1],[2,3]]
	b=copy.deepcopy(a)
	#b=a
	a[0]=[4,5]
	assert(a==[[4,5],[2,3]])
	assert(b==[[0,1],[2,3]])
#----------------------------------------------------------
if __name__=='__main__':
	copy_test()
	deepcopy_test()
