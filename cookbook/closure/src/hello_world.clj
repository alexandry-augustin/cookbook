;; Add two numbers
(+ 1 1)

;; Subtract: "10 - 3"
(- 10 3)

;; Multiply, divide
(* 2 5)
(/ 10.0 3.3)

;; Complex arithmetic expressions are built up like so:
;; (10 * (2.1 + 4.3) / 2)
(/ (* 10 (+ 2.1 4.3)) 2)

;; Anglican supplies functions for `log`, `exp`, and more:
(exp -2)
(log (+ 1 1))
(sqrt 5)
