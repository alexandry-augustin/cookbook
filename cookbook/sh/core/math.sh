#!/bin/sh
#----------------------------------------------------------
main()
{
	N=0
	SUM=0
	for DATA in `cat data`
	do
		SUM=`expr $SUM + $DATA`
		N=`expr $N + 1`
	done
	AVG=`expr $SUM / $N`
	echo "$SUM / $N = $AVG"
	#--------------------------------------------------
	# cf bc command
	#--------------------------------------------------
	echo $((1+1))
	N=4
	M=2
	echo $(($N/$M))
}
#----------------------------------------------------------
main "$@"
